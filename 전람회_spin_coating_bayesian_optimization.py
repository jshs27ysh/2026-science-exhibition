class SpinCoatingBO:
    """
    스핀코팅 조건 최적화를 위한 Bayesian Optimization 시스템.
    """

    def __init__(self, search_space, random_state=42):
        self.search_space = search_space

        self.optimizer = Optimizer(
            dimensions=search_space,
            base_estimator="GP",
            acq_func="EI",
            random_state=random_state
        )

        self.evaluated_conditions = set()

    @staticmethod
    def rpm_to_unit(rpm):
        return int(round(rpm / RPM_STEP))

    @staticmethod
    def unit_to_rpm(rpm_unit):
        return int(rpm_unit * RPM_STEP)

    @staticmethod
    def condition_to_real_values(condition):
        concentration, rpm_unit, time = condition
        rpm = SpinCoatingBO.unit_to_rpm(int(rpm_unit))

        return [int(concentration), int(rpm), int(time)]

    def tell_dataframe(self, df):
        required_columns = ["concentration", "rpm", "time", "J_distance"]

        for col in required_columns:
            if col not in df.columns:
                raise ValueError(f"필수 컬럼이 없습니다: {col}")

        for _, row in df.iterrows():
            condition_for_bo = [
                int(row["concentration"]),
                self.rpm_to_unit(int(row["rpm"])),
                int(row["time"])
            ]

            objective_value = float(row["J_distance"])

            real_condition_tuple = (
                int(row["concentration"]),
                int(row["rpm"]),
                int(row["time"])
            )

            if real_condition_tuple not in self.evaluated_conditions:
                self.optimizer.tell(condition_for_bo, objective_value)
                self.evaluated_conditions.add(real_condition_tuple)

    def ask_unique(self, n_points=3, max_trials=200):
        recommendations = []
        recommendation_set = set()

        trials = 0

        while len(recommendations) < n_points and trials < max_trials:
            candidate_for_bo = self.optimizer.ask()
            real_candidate = self.condition_to_real_values(candidate_for_bo)
            candidate_tuple = tuple(real_candidate)

            already_evaluated = candidate_tuple in self.evaluated_conditions
            already_recommended = candidate_tuple in recommendation_set

            if not already_evaluated and not already_recommended:
                recommendations.append(real_candidate)
                recommendation_set.add(candidate_tuple)

            trials += 1

        if len(recommendations) < n_points:
            extra_conditions = self.get_random_remaining_conditions(
                n_needed=n_points - len(recommendations),
                existing_recommendations=recommendation_set
            )
            recommendations.extend(extra_conditions)

        return recommendations

    def get_random_remaining_conditions(
        self,
        n_needed,
        existing_recommendations=None,
        max_trials=10000
    ):
        if existing_recommendations is None:
            existing_recommendations = set()

        remaining = []
        trials = 0

        while len(remaining) < n_needed and trials < max_trials:
            concentration = np.random.randint(CONCENTRATION_MIN, CONCENTRATION_MAX + 1)
            rpm = random.randrange(RPM_MIN, RPM_MAX + RPM_STEP, RPM_STEP)
            time = np.random.randint(TIME_MIN, TIME_MAX + 1)

            condition = [concentration, rpm, time]
            condition_tuple = tuple(condition)

            if (
                condition_tuple not in self.evaluated_conditions
                and condition_tuple not in existing_recommendations
            ):
                remaining.append(condition)
                existing_recommendations.add(condition_tuple)

            trials += 1

        return remaining


