class ObjectiveCalculator:
    """
    CV, RNU, Gr를 initial 데이터 기준으로 정규화하고
    J_distance, J_max를 계산한다.

    이 코드에서 J_distance는 실제 거리값이 아니라
    CV_norm, RNU_norm, Gr_norm의 1:1:1 동일 가중 평균 목적함수 J이다.

    음수 정규화값도 그대로 반영되므로,
    initial 기준 최솟값보다 개선된 조건은 J_distance가 음수로 나타날 수 있다.
    """

    def __init__(self):
        self.ranges = None

    def fit(self, initial_df):
        required_columns = ["CV", "RNU", "Gr"]
        self._check_columns(initial_df, required_columns)

        self.ranges = {}

        for col in required_columns:
            min_value = initial_df[col].min()
            max_value = initial_df[col].max()

            if max_value == min_value:
                max_value = min_value + 1e-12

            self.ranges[col] = {
                "min": min_value,
                "max": max_value
            }

    def normalize_value(self, value, col):
        if self.ranges is None:
            raise RuntimeError("먼저 fit(initial_df)을 실행해야 합니다.")

        min_value = self.ranges[col]["min"]
        max_value = self.ranges[col]["max"]

        return (value - min_value) / (max_value - min_value)

    def transform(self, df):
        required_columns = ["CV", "RNU", "Gr"]
        self._check_columns(df, required_columns)

        result_df = df.copy()

        result_df["CV_norm"] = result_df["CV"].apply(lambda x: self.normalize_value(x, "CV"))
        result_df["RNU_norm"] = result_df["RNU"].apply(lambda x: self.normalize_value(x, "RNU"))
        result_df["Gr_norm"] = result_df["Gr"].apply(lambda x: self.normalize_value(x, "Gr"))

        # ------------------------------------------------------------
        # 수정된 부분
        # ------------------------------------------------------------
        # 기존 코드:
        # result_df["J_distance"] = sqrt(CV_norm^2 + RNU_norm^2 + Gr_norm^2)
        #
        # 수정 코드:
        # CV_norm : RNU_norm : Gr_norm = 1 : 1 : 1 동일 가중치
        # 음수 정규화값도 그대로 반영한다.
        #
        # 따라서 initial 기준 최솟값보다 더 좋은 값이 나오면
        # J_distance가 음수로 나타날 수 있다.
        # ------------------------------------------------------------
        result_df["J_distance"] = (
            result_df["CV_norm"] +
            result_df["RNU_norm"] +
            result_df["Gr_norm"]
        ) / 3

        result_df["J_max"] = result_df[["CV_norm", "RNU_norm", "Gr_norm"]].max(axis=1)

        return result_df

    @staticmethod
    def _check_columns(df, required_columns):
        missing_columns = [col for col in required_columns if col not in df.columns]

        if missing_columns:
            raise ValueError(f"필수 컬럼이 없습니다: {missing_columns}")


