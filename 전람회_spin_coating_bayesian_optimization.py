def plot_bo_progress(df):
    plot_df = df.reset_index(drop=True).copy()

    experiment_numbers = np.arange(1, len(plot_df) + 1)
    j_values = plot_df["J_distance"].values
    best_so_far = np.minimum.accumulate(j_values)

    plt.figure(figsize=(8, 5))
    plt.plot(experiment_numbers, j_values, marker="o", label="Measured J_distance")
    plt.plot(experiment_numbers, best_so_far, marker="s", label="Best J_distance so far")
    plt.xlabel("Experiment number")
    plt.ylabel("J_distance")
    plt.title("Bayesian Optimization Progress")
    plt.legend()
    plt.grid(True)
    plt.show()


def plot_objective_scatter(df):
    pareto_df = find_pareto_front(df, ["CV", "RNU", "Gr"])

    pairs = [
        ("CV", "RNU"),
        ("CV", "Gr"),
        ("RNU", "Gr")
    ]

    for x_col, y_col in pairs:
        plt.figure(figsize=(6, 5))
        plt.scatter(df[x_col], df[y_col], label="All conditions")
        plt.scatter(
            pareto_df[x_col],
            pareto_df[y_col],
            s=100,
            marker="s",
            label="Pareto candidates"
        )
        plt.xlabel(x_col)
        plt.ylabel(y_col)
        plt.title(f"{x_col} vs {y_col}")
        plt.legend()
        plt.grid(True)
        plt.show()


