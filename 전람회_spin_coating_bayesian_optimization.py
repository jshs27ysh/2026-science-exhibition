def load_experiment_excel(excel_path, sheet_name=0):
    """
    엑셀 파일에서 stage, 농도, rpm, 시간, CV, RNU, Gr 값을 읽어온다.

    허용 열 이름:
    - stage, Stage
    - 농도, 농도값, concentration, Concentration
    - rpm, RPM
    - 시간, 시간값, time, Time
    - CV, cv
    - RNU, rnu
    - Gr, GR, gr
    """

    if not os.path.exists(excel_path):
        raise FileNotFoundError(f"파일을 찾을 수 없습니다: {excel_path}")

    df = pd.read_excel(excel_path, sheet_name=sheet_name)

    df.columns = [str(col).strip() for col in df.columns]

    rename_dict = {
        "Stage": "stage",
        "stage": "stage",

        "농도": "concentration",
        "농도값": "concentration",
        "concentration": "concentration",
        "Concentration": "concentration",

        "RPM": "rpm",
        "rpm": "rpm",

        "시간": "time",
        "시간값": "time",
        "time": "time",
        "Time": "time",

        "CV": "CV",
        "cv": "CV",

        "RNU": "RNU",
        "rnu": "RNU",

        "Gr": "Gr",
        "GR": "Gr",
        "gr": "Gr"
    }

    df = df.rename(columns=rename_dict)

    if "stage" not in df.columns:
        df["stage"] = "initial"

    required_columns = ["stage", "concentration", "rpm", "time", "CV", "RNU", "Gr"]

    missing = [col for col in required_columns if col not in df.columns]
    if missing:
        raise ValueError(f"엑셀 파일에 필요한 열이 없습니다: {missing}")

    df = df[required_columns].copy()

    numeric_columns = ["concentration", "rpm", "time", "CV", "RNU", "Gr"]

    for col in numeric_columns:
        df[col] = pd.to_numeric(df[col], errors="coerce")

    df = df.dropna(subset=numeric_columns).reset_index(drop=True)

    df["concentration"] = df["concentration"].astype(int)
    df["rpm"] = df["rpm"].astype(int)
    df["time"] = df["time"].astype(int)

    invalid_concentration = df[
        (df["concentration"] < CONCENTRATION_MIN) |
        (df["concentration"] > CONCENTRATION_MAX)
    ]

    invalid_rpm = df[
        (df["rpm"] < RPM_MIN) |
        (df["rpm"] > RPM_MAX) |
        (df["rpm"] % RPM_STEP != 0)
    ]

    invalid_time = df[
        (df["time"] < TIME_MIN) |
        (df["time"] > TIME_MAX)
    ]

    if len(invalid_concentration) > 0:
        raise ValueError(
            f"농도값이 {CONCENTRATION_MIN}~{CONCENTRATION_MAX} wt% 범위를 벗어난 행이 있습니다."
        )

    if len(invalid_rpm) > 0:
        raise ValueError(
            f"rpm 값이 {RPM_MIN}~{RPM_MAX} 범위를 벗어나거나 {RPM_STEP} rpm 단위가 아닌 행이 있습니다."
        )

    if len(invalid_time) > 0:
        raise ValueError(
            f"시간값이 {TIME_MIN}~{TIME_MAX} s 범위를 벗어난 행이 있습니다."
        )

    df["stage"] = df["stage"].astype(str)

    return df


