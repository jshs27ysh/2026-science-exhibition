def make_next_stage_name(master_df):
    """
    master 데이터에 이미 존재하는 bo_round 번호를 확인하고
    다음 stage 이름을 자동 생성한다.
    """

    if "stage" not in master_df.columns:
        return "bo_round_1"

    stages = master_df["stage"].astype(str).tolist()
    round_numbers = []

    for stage in stages:
        if stage.startswith("bo_round_"):
            try:
                number = int(stage.replace("bo_round_", ""))
                round_numbers.append(number)
            except ValueError:
                pass

    if len(round_numbers) == 0:
        return "bo_round_1"

    return f"bo_round_{max(round_numbers) + 1}"


def update_master_excel(
    base_excel_path,
    master_excel_path,
    new_excel_path=None,
    sheet_name=0
):
    """
    기존 데이터와 새 BO 실험 데이터를 누적하여 master 엑셀을 갱신한다.

    작동 방식:
    1. master 파일이 없으면 base 파일을 읽어서 master를 만든다.
    2. new_excel_path가 있으면 그 파일의 새 실험 데이터를 master에 추가한다.
    3. 같은 농도, rpm, 시간 조건이 중복되면 마지막 데이터를 남긴다.
    4. 갱신된 master 데이터를 반환한다.
    """

    if os.path.exists(master_excel_path):
        master_df = load_experiment_excel(master_excel_path, sheet_name)
        print("기존 master 파일을 불러왔습니다.")

    else:
        master_df = load_experiment_excel(base_excel_path, sheet_name)

        # 처음 데이터는 stage를 initial로 고정
        master_df["stage"] = "initial"

        master_df.to_excel(master_excel_path, index=False)
        print("master 파일이 없어서 base 데이터로 새로 생성했습니다.")

    if new_excel_path is None:
        print("새 데이터 파일이 지정되지 않았습니다. 기존 master 데이터만 사용합니다.")
        print(f"현재 누적 실험 수: {len(master_df)}")
        return master_df

    if not os.path.exists(new_excel_path):
        print(f"새 데이터 파일을 찾을 수 없습니다: {new_excel_path}")
        print("기존 master 데이터만 사용합니다.")
        print(f"현재 누적 실험 수: {len(master_df)}")
        return master_df

    new_df = load_experiment_excel(new_excel_path, sheet_name)

    next_stage = make_next_stage_name(master_df)

    # 새 데이터의 stage가 비어 있거나 initial이면 자동으로 bo_round_n 부여
    new_df["stage"] = new_df["stage"].astype(str)

    invalid_stage_mask = (
        new_df["stage"].isna()
        | (new_df["stage"].str.lower() == "nan")
        | (new_df["stage"].str.strip() == "")
        | (new_df["stage"].str.lower() == "initial")
    )

    new_df.loc[invalid_stage_mask, "stage"] = next_stage

    combined_df = pd.concat([master_df, new_df], ignore_index=True)

    # 같은 조건이 중복되면 마지막 데이터 유지
    combined_df = combined_df.drop_duplicates(
        subset=["concentration", "rpm", "time"],
        keep="last"
    ).reset_index(drop=True)

    combined_df.to_excel(master_excel_path, index=False)

    print("새 데이터가 master 파일에 추가되었습니다.")
    print(f"이번 추가 stage: {next_stage}")
    print(f"현재 누적 실험 수: {len(combined_df)}")
    print(f"master 저장 위치: {master_excel_path}")

    return combined_df


