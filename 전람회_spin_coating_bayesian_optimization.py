def is_dominated(row, others, objectives):
    for _, other in others.iterrows():
        better_or_equal_all = all(other[obj] <= row[obj] for obj in objectives)
        strictly_better_one = any(other[obj] < row[obj] for obj in objectives)

        if better_or_equal_all and strictly_better_one:
            return True

    return False


def find_pareto_front(df, objectives):
    pareto_indices = []

    for index, row in df.iterrows():
        others = df.drop(index)

        if not is_dominated(row, others, objectives):
            pareto_indices.append(index)

    return df.loc[pareto_indices].reset_index(drop=True)


def select_balanced_pareto_candidate(pareto_df):
    if pareto_df.empty:
        raise ValueError("파레토 후보가 없습니다.")

    sorted_df = pareto_df.sort_values(
        by=["J_max", "J_distance"],
        ascending=[True, True]
    )

    return sorted_df.iloc[0]


