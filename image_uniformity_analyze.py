# 6. 이미지 1장에 대해 고리별 green_intensity 평균 계산
# ============================================================

def calculate_ring_mean_green_intensity(image_path):
    image_path = Path(image_path)

    # 한글 경로도 읽을 수 있도록 처리
    img_array = np.fromfile(str(image_path), np.uint8)
    img_bgr = cv2.imdecode(img_array, cv2.IMREAD_COLOR)

    if img_bgr is None:
        raise ValueError(f"이미지를 읽을 수 없습니다: {image_path}")

    img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

    # Green 채널 추출
    G = img_rgb[:, :, 1]

    # 이미지 중심을 원의 중심으로 설정
    height, width = G.shape
    x_center = width // 2
    y_center = height // 2

    radius = min(
        x_center,
        y_center,
        width - 1 - x_center,
        height - 1 - y_center
    )

    edge_exclusion_ratio = 1.00
    sample_radius = radius * edge_exclusion_ratio

    # 고리 설정
    num_rings = 100

    ring_mean_results = []

    for ring in range(1, num_rings + 1):
        # 기존 방식 유지:
        # 가장자리로 갈수록 고리 면적이 커지므로 점 개수를 증가시킴
        points_in_this_ring = 10 * (2 * ring - 1)

        # 각 고리의 가운데 반지름 위치
        r_norm = (ring - 0.5) / num_rings
        r = sample_radius * r_norm

        intensities = []

        for j in range(points_in_this_ring):
            theta = 2 * np.pi * j / points_in_this_ring

            # 고리마다 각도를 살짝 회전시켜 점들이 일직선으로 겹쳐 보이는 것 방지
            theta += ring * np.pi / points_in_this_ring

            x = int(round(x_center + r * np.cos(theta)))
            y = int(round(y_center + r * np.sin(theta)))

            x = np.clip(x, 0, width - 1)
            y = np.clip(y, 0, height - 1)

            intensities.append(float(G[y, x]))

        mean_green_intensity = np.mean(intensities)

        ring_mean_results.append({
            "ring": ring,
            "r_norm": r_norm,
            "points_in_ring": points_in_this_ring,
            "mean_green_intensity": mean_green_intensity
        })

    ring_mean_df = pd.DataFrame(ring_mean_results)

    rpm, time, concentration, repeat = parse_filename_condition(image_path)

    ring_mean_df.insert(0, "filename", image_path.name)
    ring_mean_df.insert(1, "rpm", rpm)
    ring_mean_df.insert(2, "time", time)
    ring_mean_df.insert(3, "concentration", concentration)
    ring_mean_df.insert(4, "repeat", repeat)

    return ring_mean_df


