# 5. 파일명에서 rpm, 시간, 농도, 반복번호 추출
# ============================================================

def parse_filename_condition(image_path):
    """
    파일명 예시:
    500 10 4 1_circle_crop.png

    의미:
    rpm = 500
    time = 10
    concentration = 4
    repeat = 1
    """

    stem = Path(image_path).stem

    cleaned = stem.replace("_circle_crop", "")
    cleaned = cleaned.replace("circle_crop", "")

    numbers = re.findall(r"\d+", cleaned)

    if len(numbers) < 4:
        raise ValueError(
            f"파일명에서 rpm, 시간, 농도, 반복번호를 읽을 수 없습니다: {Path(image_path).name}"
        )

    rpm = int(numbers[0])
    time = int(numbers[1])
    concentration = int(numbers[2])
    repeat = int(numbers[3])

    return rpm, time, concentration, repeat


